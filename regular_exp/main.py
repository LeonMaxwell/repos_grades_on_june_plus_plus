import re


def main():
    print(re.findall(r'[+]?\d{1,3}\(?\d{3}\)?(?:-?\d{2,3}){3}', r'Мой номер +7(978)567-21-11 или мой номер 89785672111 или мой украинский номер +380(987)876-24-31'))
    print(re.sub(r'\s+', r' ', "Это строка    с многими пробелами  и вот еще  да!"))
    print(re.sub(r'(\d{4}).(\d\d).(\d\d)', r'\3/\2/\1', "Это произошло 2022.12.14"))


if __name__ == '__main__':
    main()